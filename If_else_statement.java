


//If_else_statement contain true or false value
public class If_else_statement {
    public static void main(String[] args) {
        int a=10,b=20;
        if(a > b){
            System.out.println("a is greater ="+a);
        }
        else{
            System.out.println("b is greater ="+b);
        }
    }
    
}
