
// Assignment Operators( =,+=,-=,*=,/=,%=,&=,^=,|=,>>=,<<=)
public class Assignment_Operators {
    public static void main(String[] args) {
        
        int a = 10; // =
            a += 2; // a = a + 2
            System.out.println(a);
            
            a -= 2; // a = a - 2
            System.out.println(a);
            
            a *= 2; // a = a * 2
            System.out.println(a);
            
            a /= 2; // a = a / 2
            System.out.println(a);
            
            a %= 2; // a = a % 2
            System.out.println(a);
          
            int b= 30;
            b &= 2; // a = a & 2
            System.out.println(b);
            int c=20;
            c ^= 2; // a = a ^ 2
            System.out.println(c);
            
            a |= 2; // a = a | 2
            System.out.println(a);
            
            a >>= 2; // a = a >> 2
            System.out.println(a);
            
            a <<= 2; // a = a << 2
            System.out.println(a);
        
        
       
        
    }
    
    
}
