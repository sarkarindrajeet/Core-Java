

// Logical operators( &&, ||, !)
public class Logical_Operators {
    public static void main(String[] args) {
        
        int a=10 , b= 7;
        
        boolean c= a > 8 && b > 8;
        System.out.println(c);
        
        c = a > 8 || b > 8;
        System.out.println(c);
        
        c= (a < 8 != b < 8);
        System.out.println(c);

         
    }
    
}
