
// Comparison operators( == , != , > , < , >= , <= )
public class Comparison_Operators {
    public static void main(String[] args) {
        int  a=10, b= 5;
        boolean ans= a == b;
        System.out.println(ans); // false
        
        ans = a != b;
        System.out.println(ans); // true
        
        ans = a > b;
        System.out.println(ans); // true
        
        ans = a < b;
        System.out.println(ans); // false
        
        ans = a >= b;
        System.out.println(ans); // true
        
        ans = a <= b;
        System.out.println(ans); // false
        
    }
    
}
